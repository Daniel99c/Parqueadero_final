    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        DAOvehiculos vehCont = VehiculosController .getInstance();
        String placa = TexFiePlaca.getText();
        String nombrePropietario = TexFieNombreP.getText();
       
        String tipoVehiculo = "";
        if (Ch_Motocicleta.isSelected())
            tipoVehiculo += "Motocicleta";
        
         if (Ch_Automovil.isSelected())
            tipoVehiculo += "Automovil";
        
        String [] vehiculo = {placa,nombrePropietario,tipoVehiculo};
        if (vehCont.ActualizarVehiculo(vehiculo))
            
            JOptionPane.showMessageDialog(this, "Actualizacion exitosa"); 
    }//GEN-LAST:event_btnActualizarActionPerformed

