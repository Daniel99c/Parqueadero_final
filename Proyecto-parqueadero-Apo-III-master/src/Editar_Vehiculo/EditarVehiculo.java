     public void validaCampos (){
        if ( TexFiePlaca.getText().isEmpty()){
            lblvali5.setText("Campo Requerido (*)");
        }else{
            lblvali5.setText(" ");
        }
        if (TexFieNombreP.getText().isEmpty()){
            lblvali6.setText("Campo Requerido (*)");
        }else{
            lblvali6.setText(" ");
        }
        String miTipo = " ";
        if (Ch_Motocicleta.isSelected()|| Ch_Automovil.isSelected()){
            miTipo = " lleno ";
            lblvali7.setText(" ");
            
        }else{
            lblvali7.setText("Seleccione uno de los Tipos (*)");
        }
        if (TexFiePlaca.getText().isEmpty()|| TexFieNombreP.getText().isEmpty()|| miTipo.equals(" ")){
            btnActualizar.setEnabled(false);
        }else{
           btnActualizar.setEnabled(true);
        }
                
    }
          
