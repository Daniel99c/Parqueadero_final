    private void btnGuadarRegresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuadarRegresarActionPerformed
        // TODO add your handling code here:
        DAOvehiculos vehCont = VehiculosController .getInstance();
        String placa = TexFiePlaca.getText();
        String nombrePropietario = TexFieNombreP.getText();
       
        String tipoVehiculo = "";
        if (Ch_Motocicleta.isSelected())
            tipoVehiculo += "Motocicleta";
        
         if (Ch_Automovil.isSelected())
            tipoVehiculo += "Automovil";
        
        String [] vehiculo = {placa,nombrePropietario,tipoVehiculo};
        if (vehCont.GuardarVehiculo(vehiculo))
            
            JOptionPane.showMessageDialog(this, "Vehiculo guardado con exito");
        
        
        
        
        // limpiar los campos de placa, nombre del usuario, y tipo de vehiculo
        
        TexFiePlaca.setText("");
        TexFieNombreP.setText("");
        GrupoVehiculos.clearSelection();
        
        
        VentanaPrincipal menuOne = VentanaPrincipal.getInstancia();
        menuOne.setVisible(true);
        this.dispose();
        
  
    }//GEN-LAST:event_btnGuadarRegresarActionPerformed

