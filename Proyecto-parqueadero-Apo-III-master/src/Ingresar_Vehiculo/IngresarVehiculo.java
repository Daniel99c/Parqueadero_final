    public void validaCampos (){
        if ( TexFiePlaca.getText().isEmpty()){
            lblvali1.setText("Campo Requerido (*)");
        }else{
            lblvali1.setText(" ");
        }
        if (TexFieNombreP.getText().isEmpty()){
            lblvali2.setText("Campo Requerido (*)");
        }else{
            lblvali2.setText(" ");
        }
        String miTipo = " ";
        if (Ch_Motocicleta.isSelected()|| Ch_Automovil.isSelected()){
            miTipo += "lleno";
            lblvali3.setText(" ");
            
        }else{
            lblvali3.setText("Seleccione uno de los Tipos (*)");
        }
        if (TexFiePlaca.getText().isEmpty()|| TexFieNombreP.getText().isEmpty()|| miTipo.equals(" ")){
            btnGuadarRegresar.setEnabled(false);
        }else{
            btnGuadarRegresar.setEnabled(true);
        }
                
    }
